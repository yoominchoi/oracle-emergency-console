def fetch_incidents():
    conn = get_db_connection()
    cursor = conn.cursor()
    
    cursor.execute("""
        SELECT i.timestamp, u.name as updated_by, i.shooter_location, i.alert_msg 
        FROM incident i 
        LEFT JOIN users u ON i.updated_by = u.id 
        ORDER BY i.timestamp DESC
    """)
    rows = cursor.fetchall()
    cursor.close()
    conn.close()
    
    return rows

